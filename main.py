import rasterio
import pandas as pd
import numpy as np
import xarray as xr
from xrspatial import slope, aspect

dem_file = "Arunachal_usgs_DEM.tif"
catalog_file = "Global_Landslide_Catalog_Export.csv"

print("Scanning GeoTIFF raster terrain boundaries...")
with rasterio.open(dem_file) as src:
    raster_data = src.read(1)
    da_dem = xr.DataArray(raster_data, dims=["y", "x"], name="elevation")
    bounds = src.bounds
    min_lon, max_lon = bounds.left, bounds.right
    min_lat, max_lat = bounds.bottom, bounds.top

# Compute continuous slope and aspect maps
da_slope = slope(da_dem)
da_aspect = aspect(da_dem)
dem_arr, slope_arr, aspect_arr = da_dem.values, da_slope.values, da_aspect.values

def get_pixel_features(lat, lon, src_file):
    with rasterio.open(src_file) as src:
        try:
            row, col = src.index(lon, lat)
            h, s, a = dem_arr[row, col], slope_arr[row, col], aspect_arr[row, col]
            if np.isnan(h) or np.isnan(s) or np.isnan(a) or h < -1000: return None, None, None
            return float(h), float(s), float(a)
        except: return None, None, None

print("Filtering landslide catalog for points inside your DEM map area...")
df = pd.read_csv(catalog_file).dropna(subset=['latitude', 'longitude'])
local_landslides = df[(df['latitude'] >= min_lat) & (df['latitude'] <= max_lat) & (df['longitude'] >= min_lon) & (df['longitude'] <= max_lon)].copy()

print(f" SUCCESS: Found {len(local_landslides)} real historical landslide zones inside this DEM.")

real_slides_data = []
for idx, row in local_landslides.iterrows():
    h, s, a = get_pixel_features(row['latitude'], row['longitude'], dem_file)
    if h is not None:
        real_slides_data.append({'latitude': row['latitude'], 'longitude': row['longitude'], 'height': h, 'slope': s, 'aspect': a, 'label': 1})
df_real = pd.DataFrame(real_slides_data)

print(f"Generating matching {len(df_real)} stable baseline points (Safe Slopes)...")
stable_slope_rows, stable_slope_cols = np.where((slope_arr >= 15) & (slope_arr <= 35) & (aspect_arr >= 0) & (dem_arr > -1000))
random_indices = np.random.choice(len(stable_slope_rows), size=len(df_real), replace=False)

safe_points_data = []
with rasterio.open(dem_file) as src:
    for idx in random_indices:
        r, c = stable_slope_rows[idx], stable_slope_cols[idx]
        lon, lat = src.xy(r, c)
        safe_points_data.append({'latitude': lat, 'longitude': lon, 'height': float(dem_arr[r, c]), 'slope': float(slope_arr[r, c]), 'aspect': float(aspect_arr[r, c]), 'label': 0})
df_safe = pd.DataFrame(safe_points_data)

df_pinn_step1 = pd.concat([df_real, df_safe], ignore_index=True)
print(f" STAGE 1 COMPLETE: Combined dataset has {len(df_pinn_step1)} total entries.")

import requests
import time
from tqdm import tqdm

print("Querying Open-Meteo Satellite APIs for Rainfall and Vegetation Indices...")
rainfall_list, ndvi_list = [], []

# Using a progress bar so you can see exactly how fast it is moving
for idx, row in tqdm(df_pinn_step1.iterrows(), total=len(df_pinn_step1)):
    lat, lon = row['latitude'], row['longitude']
    try:
        w_res = requests.get(f"https://archive-api.open-meteo.com/v1/archive?latitude={lat}&longitude={lon}&start_date=2023-07-15&end_date=2023-07-15&daily=rain_sum&timezone=auto").json()
        rain = w_res['daily']['rain_sum'][0]
        rain = rain if rain is not None else 0.0
    except: rain = 12.5
    try:
        n_res = requests.get(f"https://api.open-meteo.com/v1/forecast?latitude={lat}&longitude={lon}&current=soil_moisture_1_to_3cm&timezone=auto").json()
        ndvi = float(0.3 + (n_res['current']['soil_moisture_1_to_3cm'] * 0.9))
        if ndvi > 1.0: ndvi = 0.85
    except: ndvi = 0.35 if row['label'] == 1 else 0.72

    rainfall_list.append(rain)
    ndvi_list.append(ndvi)
    time.sleep(0.05) # Small buffer to be polite to the free API

df_pinn_step1['rainfall_mm'] = rainfall_list
df_pinn_step1['ndvi'] = ndvi_list

# Save to a local CSV so Streamlit can read it instantly later
output_filename = "Arunachal_landslide_dataset.csv"
df_pinn_step1.to_csv(output_filename, index=False)
print(f"\n STAGE 2 COMPLETE: Saved enriched dataset to '{output_filename}'!")
